def main() -> None:
    frame = pd.read_csv(DATA_PATH, encoding="utf-8-sig")
    existing = set(frame["name"].fillna("").astype(str))
    additions = [item for item in ROWS if item["name"] not in existing]
    if not additions:
        print("没有新增餐厅，所有店名已存在。")
        return
    next_id = int(pd.to_numeric(frame["id"], errors="coerce").max()) + 1
    for offset, item in enumerate(additions):
        item["id"] = next_id + offset
    new_frame = pd.DataFrame(additions)
    new_frame = new_frame[frame.columns]
    result = pd.concat([frame, new_frame], ignore_index=True)
    result.to_csv(DATA_PATH, index=False, encoding="utf-8-sig")
    print(f"新增餐厅: {len(additions)} 家")
    print(f"知识库总数: {len(result)} 家")
    print(f"ID范围: {next_id}-{next_id + len(additions) - 1}")


if __name__ == "__main__":
    main()
